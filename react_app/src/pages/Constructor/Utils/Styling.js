export function WithCoordStyle(x,y){
    return(
        {
            position: "relative",
            left: x,
            top: y
        }
    )
}
export function WithCoordStyleViewElement(x,y){
    return(
        {
            position: "relative",
            left: x,
            top: y,
            width: "75px",
            height: "30px" 
        }
    )
}